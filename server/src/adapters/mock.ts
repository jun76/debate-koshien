export class MockAdapter implements AgentAdapter {
  readonly provider = "mock";

  async invoke(inv: AgentInvocation): Promise<AgentResult> {
    const started = Date.now();
    const h = inv.mockHints ?? {};
    const lang = langOf(h);
    const seed = hash32(`${inv.matchId}:${inv.agent.id}:${inv.kind}:${h.part?.id ?? ""}:${h.exchangeIndex ?? 0}`);
    // Wait a little so progress is visible in the spectator UI.
    await sleep(250 + (seed % 400));

    const topic = h.topic ?? (lang === "en" ? "the resolution" : "論題");
    let output = "";
    switch (inv.kind) {
      case "prep-research":
        output =
          lang === "en"
            ? [
                `# Research notes (${inv.agent.name})`,
                `- Organized ${sideLabel(h.side ?? "affirmative", lang)} candidate points for the resolution "${topic}"`,
                `- Promising angles: ${anglesOf(h.side ?? "affirmative", lang).slice(0, 3).join(" / ")}`,
                `- Opponent's likely claims: ${anglesOf(h.side === "negative" ? "affirmative" : "negative", lang).slice(0, 2).join(" / ")}`,
                `- Source candidates: various survey reports from the Mock Research Institute`,
              ].join("\n")
            : [
                `# 調査メモ（${inv.agent.name}）`,
                `- 論題「${topic}」について${sideLabel(h.side ?? "affirmative", lang)}視点の論点候補を整理`,
                `- 有望な角度: ${anglesOf(h.side ?? "affirmative", lang).slice(0, 3).join(" / ")}`,
                `- 相手の想定主張: ${anglesOf(h.side === "negative" ? "affirmative" : "negative", lang).slice(0, 2).join(" / ")}`,
                `- 出典候補: モック総合研究所の各種調査報告`,
              ].join("\n");
        break;
      case "prep-compile":
      case "prep-fix": {
        const side = h.side ?? "affirmative";
        fs.writeFileSync(
          path.join(inv.workspaceDir, "evidence.json"),
          JSON.stringify(mockEvidence(topic, side, lang), null, 2),
          "utf8",
        );
        fs.writeFileSync(path.join(inv.workspaceDir, "handout.md"), mockHandoutMd(topic, side, lang), "utf8");
        const p = evidencePrefix(side);
        const ids = Array.from({ length: 6 }, (_, i) => `${p}-${String(i + 1).padStart(2, "0")}`).join(", ");
        output = lang === "en" ? `Created: ${ids}` : `作成済み: ${ids}`;
        break;
      }
      case "speech":
      case "draft":
        output = h.part?.kind === "constructive" ? mockConstructive(h) : mockRebuttal(h, seed);
        if (inv.kind === "draft") output += lang === "en" ? `\n(${inv.agent.name}'s draft)` : `\n（${inv.agent.name} の草案）`;
        break;
      case "merge": {
        // The drafts are in the instructions, but the mock just adds a note to its own generation.
        const base = h.part?.kind === "constructive" ? mockConstructive(h) : mockRebuttal(h, seed);
        output = base + (lang === "en" ? `\n(Final version after team deliberation)` : `\n（チーム合議を経た最終版）`);
        break;
      }
      case "question":
        output = mockQuestion(h, seed + (h.exchangeIndex ?? 0));
        break;
      case "answer":
        output = mockAnswer(h, seed + (h.exchangeIndex ?? 0));
        break;
      case "regenerate":
        output = truncateChars(inv.instructions.split(PREVIOUS_DRAFT_MARKER)[1] ?? "", (h.maxChars ?? 400) - 10).trim();
        break;
      case "judge":
        await sleep(500);
        output = mockJudgeJson(h, seed);
        break;
      case "review":
        await sleep(500);
        output = mockReviewJson(h);
        break;
    }

    return { output, toolUsage: [], durationMs: Date.now() - started };
  }
}
